// 모듈화 하여 추후 관리용이
// 모듈화 하지 않고 각 파일 내에서 사용한다면 코드가 길어질 수 있음
// try catch 문을 사용하여 에러처리하는 과정에서 각 응답코드에 대한 에러처리를 매번 해줘야함
// 모듈화 후 추후 인터셉터 사용해서 에러처리를 한번에 처리할 수 있음
