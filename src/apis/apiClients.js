// 인터셉터란? 요청 객체와 응답 객체를 중간에서 가로채서 추가적인 처리를 할 수 있게 해주는 기능
// 인터셉터를 사용하면 요청을 보내기 전에 특정 작업을 처리하고 응답을 받아오기 전에 특정 작업을 처리할 수 있음
